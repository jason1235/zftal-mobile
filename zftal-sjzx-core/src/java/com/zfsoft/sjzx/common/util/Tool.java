	/**
	 * @description：appKey生成策略
	 * @param count
	 *            当天申请记录条数(0:当天没有申请记录)
	 * @return
	 * @author dingsj
	 */
	public static String getAppKey(int count) {
		String currentDate = appKey_numberToWord(Tool.getCurrentTime("yyyyMMdd"));
		String temp = "";
		for (int i = 0; i < 4; i++) {
			int intValue = (int) (Math.random() * 26 + 97);
			temp = temp + (char) intValue;
		}

		/* 当天没有申请记录 */
		if (count <= 0) {
			return currentDate + temp + parseKey(1);
		} else {/* 当天有申请记录 */
			return currentDate + temp + parseKey(++count);
		}
	}

	/**
	 * @description：将appKey中的数据转换成字母
	 * @return
	 * @author dingsj
	 * @date 2015-1-8
	 */
	private static String appKey_numberToWord(String arg) {
		StringBuffer s = new StringBuffer();
		for (byte b : arg.getBytes()) {
			// System.out.print((char) (b + 48));
			if (b == 48) {
				s.append(0);
			} else {
				s.append((char) (b + 48));
			}
		}
		return s.toString();
	}

	/**
	 * @description：处理appkey的后三位，不足三位的用0补充
	 * @param key
	 * @return
	 */
	private static String parseKey(int key) {
		/* 处理appkey的后三位，不足三位的用0补充 */
		if (key < 10) {
			return "00" + key;
		} else if (key < 100) {
			return "0" + key;
		} else {
			return String.valueOf(key);
		}
	}

