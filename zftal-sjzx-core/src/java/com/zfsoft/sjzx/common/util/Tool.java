	/**
	 * 转换字段类型
	 * 
	 * @param lx
	 *            类型
	 * @param cd
	 *            长度
	 * @return
	 */
	public static String getTranslx(String lx, String cd) {
		if (StringUtil.isBlank(cd)) {
			cd = "";
		}

		if (StringUtil.isBlank(lx)) {
			return "";
		}
		if (lx.toUpperCase().equals("C")) {
			return Constants.ORACLE_VARCHAR;
		}
		if (lx.toUpperCase().equals("D")) {
			return Constants.ORACLE_DATE;
		}
		if (lx.toUpperCase().equals("N")) {
			return Constants.ORACLE_NUMBER;
		}
		if (lx.toUpperCase().equals("B")) {
			return Constants.ORACLE_BLOB;
		}
		if (lx.toUpperCase().equals("T") && cd.trim() == "") {
			return Constants.ORACLE_CLOB;
		}
		if (lx.toUpperCase().equals("T") && Integer.parseInt(cd.trim()) > 3999) {
			return Constants.ORACLE_CLOB;
		}
		if (lx.toUpperCase().equals("T") && Integer.parseInt(cd.trim()) < 3999) {
			return Constants.ORACLE_VARCHAR;
		}
		return lx;
	}


